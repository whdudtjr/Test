
public class P5 {

	public static void main(String[] args) {
		// switch ~ case: 이 문법은 아예 안써도 if로 다 대체됨
		int a = 10;
		
		switch (a) {
		
		case 7:
			System.out.println("7입니다");
			break;
		case 8:
			System.out.println("8입니다");
			break;
		case 9:
			System.out.println("9입니다");
			break;
		case 10:
			System.out.println("10입니다");
			break;
		default:
			System.out.println("그 외입니다");
			break;
		}

	}

}
