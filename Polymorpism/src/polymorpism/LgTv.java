package polymorpism;

public class LgTv implements Tv {
	public void powerOn() {
		System.out.println("엘지티비 전원을 킵니다.");
	}
	public void powerOff() {
		System.out.println("엘지티비 전원을 끕니다.");
	}
	public void soundUp() {
		System.out.println("엘지티비 소리를 올립니다.");
	}
	public void soundDown() {
		System.out.println("앨지티비 소리를 내립니다.");
	}
}
